    public void setKapal(){
        Kapal ship = new Kapal();
        for (int i = 0; i < 5; i++) {
            papanKapal.add(ship.kapal[i]);
        }
        ship.kapal[0].setLocation(0, 0);
        ship.kapal[1].setLocation(40, 0);
        ship.kapal[2].setLocation(115, 0);
        ship.kapal[3].setLocation(155, 0);
        ship.kapal[4].setLocation(195, 0);
    }
    
    public void setPapan(){ //membuat papan kotak
        int posisiX=0;
        int posisiY=0;
        int size=35;
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                //papan untuk player1
                papan1[i][j] = new JButton();
                papan1[i][j].setBackground(Color.white);
                papan1[i][j].setSize(size, size);
                papan1[i][j].setLocation(posisiX, posisiY);
                player1.add(papan1[i][j]);
                posisiX+=35;
            }
            posisiX=0;
            posisiY+=35;
        }
    }

